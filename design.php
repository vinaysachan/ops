                                <h2>Basic Intro &amp; History of PHP</h2>                           
                                <div class="line"></div>
                                <div>
                                    PHP is a most popular Server-side programming language for building dynamic interactive website and web based applications & plugins. <br> PHP stands for <code>"Hypertext Pre-processor"</code> . 
                                    <br> The core purpose of PHP : " <span class="text-success">to process information and produce hypertext(HTML) as a result</span>".
                                </div>
                                <div class="mt5">
                                    Welcome to the world of PHP, one of the Web's most popular programming languages. According to Netcraft , PHP was running on more than <a href="http://news.netcraft.com/archives/2013/01/31/php-just-grows-grows.html" target="blank">244 million Web SItes and  2.1M IP addresses in January 2013 </a>.
                                </div>
                                <div class="mt5">
                                    The first version of PHP was named Personal Home Page Tools (PHP Tools) when it was released by <a href="http://en.wikipedia.org/wiki/Rasmus_Lerdorf" target="blank">Rasmus Lerdorf</a> in 1995.<br/>
                                    <span class="text-danger bolder">PHP 1 </span>can still be downloaded today from <a href="http://museum.php.net/" target="blank">museum.php.net</a>.
                                    Weighing in at only 26 kilobytes in size, php-108.tar.gz is diminutive by today's standards, yet it was capable of allowing users to implement guestbooks and other form-processing applications.<br/>
                                    <span class="text-danger bolder">PHP 2</span> introduced built-in support for accessing databases, cookie handling, and user-defined functions. It was released in 1997, and by the following year, around 1% of sites on the internet were using PHP.
                                </div>
                                <div class="mt5">
                                    <span class="text-danger bolder">PHP 3.0 </span> was the first release to closely resemble today's incarnation of PHP. A rewrite of the underlying parser by <a href="http://en.wikipedia.org/wiki/Andi_Gutmans" target="blank">Andi Gutmans</a> and <a href="http://en.wikipedia.org/wiki/Zeev_Suraski" target="blank">Zeev Suraski</a> led to what was arguably a different language; accordingly, it was renamed to simply PHP, which was a recursive acronym for "PHP: Hypertext Preprocessor". This was released in 1998 and the ease of extending the language played a large part in its tremendous success, as this aspect attracted dozens of developers to submit a variety of modules. 
                                </div>
                                <div class="mt5">
                                    <a href="http://en.wikipedia.org/wiki/Andi_Gutmans" target="blank">Andi Gutmans</a> and <a href="http://en.wikipedia.org/wiki/Zeev_Suraski" target="blank">Zeev Suraski</a> continued to rewrite PHP's core, primarily to improve performance and increase the modularity of the codebase. This led to the creation of the Zend Engine, which was used by PHP 4 when it was released in 2000. As well as offering better performance, <span class="text-danger bolder">PHP 4</span> could be used with more web servers, supported HTTP sessions, output buffering and several new language constructs.<br/> By September 2001, Netcraft's Web Server Survey found 1.8M sites running PHP.
                                </div>
                                <div class="mt5">
                                    <span class="text-danger bolder">PHP 5</span> was released in 2004, and remains the most recent major version release today  (<span class="text-danger bolder">PHP 5.6.0alpha1 </span> was <a href="http://php.net/archive/2014.php#id2014-01-23-1" target="blank">released on 23 January 2014</a>). Zend Engine 2.0 forms the core of this release.
                                </div> 
